//  comparison Operators 

let v = 5;
let m =  "5"; // string -> number 

console.log("a == b ", v == m);
console.log("a != b ", v != m);  // not equal tp !=
console.log("a === b ", v === m);
console.log("a !== b ", v !== m);

console.log(v < m);
console.log(v > m);
console.log(v <= m);
console.log(v >= m);
